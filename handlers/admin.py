def register_admin_handlers(dp: Dispatcher):
    """–†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞"""
    # –†–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥—ã /stats —Å —Ñ–∏–ª—å—Ç—Ä–∞–º–∏ Command –∏ IsAdmin
    dp.message.register(cmd_stats, Command("stats"), IsAdmin())
    # –†–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥—ã /reset_stats —Å —Ñ–∏–ª—å—Ç—Ä–∞–º–∏ Command –∏ IsAdmin
    dp.message.register(cmd_reset_stats, Command("reset_stats"), IsAdmin())
    